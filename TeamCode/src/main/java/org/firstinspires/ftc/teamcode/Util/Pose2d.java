package org.firstinspires.ftc.teamcode.Util;

public class Pose2d {

    private double xPos;
    private double yPos;

    public Pose2d(double x, double y) {
        xPos = x;
        yPos = y;
    }

    public double getXPos() {
        return xPos;
    }

    public double getYPos() {
        return yPos;
    }
}
